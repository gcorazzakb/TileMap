package com.company.Tiles;

public class TileCorner {
    float height;
    int x; int y;

    public TileCorner(float height, int x, int y) {
        this.height = height;
        this.x = x;
        this.y = y;
    }

    public float getHeight() {
        return height;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }
}
